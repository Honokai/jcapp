package entidades;

/**
 *
 * @author eff
 */
public class Curso {
    
    private Integer id;
    private String curso;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getCurso() {
        return curso;
    }

    public void setCurso(String curso) {
        this.curso = curso;
    }

    @Override
    public String toString() {
        return "ID: "+ getId() + "   ||    " 
                + "Curso: "+ getCurso();
    }
    
}
